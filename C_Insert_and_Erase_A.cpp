int gcd(int a,int b){
    if(b==0) return a;
    return gcd(b,a%b);
}

void solve(){
    string s,t;
    cin >>s>>t;

    int i=0,j=0;
    int n=s.size() , m=t.size();
    int ans=0;
    while(i<n || j <m){
        int a1=0, a2=0;
        while(i<n && s[i]=='A')a1++ , i++;
        while(j<m && t[j]=='A')a2++ , j++;
        ans+=abs(a1-a2);

        if(i<n && j <m){
            if(s[i]!=t[j]){
                cout <<-1;
                return;
            }
            i++ , j++;
        }
        else if(i<n || j <m){
            cout << -1;
            return;
        }
    }
    cout <<ans;
}

void test(){
    int t;
    cin >> t;
    while(t--){
        solve();
    }
}

