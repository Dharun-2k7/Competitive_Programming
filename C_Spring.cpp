int gcd(int a,int b){
    if(b==0) return a;
    return gcd(b,a%b);
}

int lmc(int a,int b){
    return (a/gcd(a,b))*b;
}
void solve(){
    int a,b,c,m; cin >>a>>b>>c>>m;
    int na=m/a;
    int nb=m/b;
    int nc=m/c;

    int lab=lcm(a,b);
    int lac=lcm(a,c);
    int lbc=lcm(b,c);

    int nab=m/lab;
    int nac=m/lac;
    int nbc=m/lbc;
    
    int labc=lcm(lab,c);
    int nabc=m/labc;

    int ali=6*na-3*nab-3*nac+2*nabc;
    int bob=6*nb-3*nab-3*nbc+2*nabc;
    int car=6*nc-3*nac-3*nbc+2*nabc;

    cout<<ali<<" "<<bob<<" "<<car<<nl;
}

void test(){
    int t;
    cin >> t;
    while(t--){
        solve();
    }
}

