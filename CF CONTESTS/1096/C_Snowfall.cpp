int gcd(int a,int b){
    if(b==0) return a;
    return gcd(b,a%b);
}

void solve(){
    int n; cin >> n;
    vi a(n);
    for(int &x : a) cin >> x;
    
    vi a1, b, c, d;
    for(int x : a){
        if(x % 6 == 0) a1.pb(x);
        else if(x % 2 == 0) b.pb(x);
        else if(x % 3 == 0) c.pb(x);
        else d.pb(x);
    }

    for(int x : a1) cout << x << " ";
    for(int x : b) cout << x << " ";
    for(int x : d) cout << x << " ";
    for(int x : c) cout << x << " ";
    cout << nl;
}
void test(){
    int t;
    cin >> t;
    while(t--){
        solve();
    }
}

