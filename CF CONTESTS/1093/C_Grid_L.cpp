int gcd(int a,int b){
    if(b==0) return a;
    return gcd(b,a%b);
}

void solve(){
    int p,q; cin >> p >> q;
    int k = 2*(p + 2*q) + 1;

    for(int d = 1; d*d <= k; d++){
        if(k % d) continue;
        int x = d;
        int y = k / d;
        if(x % 2 == 0 || y % 2 == 0) continue;
        int n = (x - 1) / 2;
        int m = (y - 1) / 2;
        if(n > 0 && m > 0){
            cout << n << " " << m << nl;
            return;
        }
    }
    cout << -1 << nl;
}

void test(){
    int t;
    cin >> t;
    while(t--){
        solve();
    }
}

